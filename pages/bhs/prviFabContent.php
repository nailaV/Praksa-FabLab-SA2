        <h1 class="w3-xxxlarge w3-text-ljubicasta">Prvi Fab u BiH - FabLab Sarajevo</h1>
        <hr style="width:80%;border:3px solid #7C6F97;margin: auto;" class="w3-round">

        <div id="first" class="edit">

            <p align="justify">
                <strong class="uvodFontSizeFontSize providna"> <i> „FabLab Sarajevo je tehnička platforma za izradu
                        prototipa za izume, saradnju i učenje, a kreirana je da inspiriše ljude i poduzetnike da svoje
                        ideje pretvore u nove proizvode i prototipove.“</i> </strong><br>
                <br><br>
                Naš centar izvrsnosti u Sarajevu – FabLab Sarajevo – okuplja svoje članove, kao demonstracijski i vodeći
                centar osnovan u oktobru 2020. godine, prepun tehnologije, znanja i stručnosti potrebnih za
                prevazilaženje jaza između akademske zajednice i industrije. Prvi je zvanično priznati FabLab u zemlji.
