        <div class="edit">
            <p>

                <br><br>
                FabLab Sarajevo nastoji integrisati relevantne sposobnosti i potaknuti saradnju na odgovornom
                istraživanju i inovacijama sa širom istraživačkom zajednicom i industrijom, kao i drugim tijelima javnog
                sektora, kako bi se postigli značajni i efektni rezultati.
                <br><br>
                Inženjering, istraživanje i razvoj su u samom središtu razvoja kapaciteta FabLab Sarajevo i, stoga,
                jedan od glavnih prioriteta. Aktivnosti istraživanja i razvoja FabLab Sarajevo protežu se od
                biomedicinskih nauka do fizičkih nauka i inženjerstva, istovremeno proširujući mogućnosti u nove domene
                (roboti, bio, tekstil, cirkularna ekonomija, zeleno) koje stvaraju prilike za održivi rast.
                <br><br>
                Kao podrška inovacijama, FabLab Sarajevo potiče eksperimentiranje kroz nove tehnologije kao što su
                proširena i virtuelna stvarnost i vještačka inteligencija.
                <br><br>
                Fab Lab Sarajevo oslobađa puni potencijal istraživanja i razvoja i inovacija kako bi pomogao našoj široj
                zajednici i klijentima da kreiraju prototipove i pametne proizvode, optimiziraju proizvodne operacije i
                isporuče nove izvore vrijednosti, omogućavajući uspon održive industrije digitalne proizvodnje u zemlji.
                <br><br>Najviše od svega cijenimo naše ljude i smatramo da je pravi tim oko nas jedan od naših ključnih
                „alata“. Tim se sastoji od stalnog osoblja i eksternih eksperata, ali imamo i pripravnike iz različitih
                instituta i volontere s različitim iskustvom i mogućnostima.
                <br><br>

            </p>


