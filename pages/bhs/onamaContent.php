        <span id="history"></span>
        <h2>Istorijat FabLab-a </h2>
        <p>
            <strong>„Dajte običnim ljudima prave alate i dizajnirat će i izgraditi najnevjerovatnije stvari.“</strong><br>
            <br> To je ideja koja stoji iza Fab Lab-a, koji je nastao 2001. godine pri 
            <a href="https://cba.mit.edu/" target="_blank" class="linksDark"> Centru za bitove i atome </a> na <a href="https://www.mit.edu/"
                target="_blank" class="links"> Institutu za tehnologije u Massachusettsu</a> pod vođstvom profesora<a href="https://en.wikipedia.org/wiki/Neil_Gershenfeld" class="links"
                target="_blank"> Neil Gershenfeld </a>.<br><br> 
                FabLab, ili laboratorija za digitalnu proizvodnju, mjesto je za igru, stvaranje, mentorstvo i izume te mjesto za učenje i inovacije. FabLab-ovi su također poznati kao urbane laboratorije ili laboratorije za industrijske inovacije.
        </p><br>



        <span id="network"></span>
        <h2>
            <a href="https://fablabs.io/" target="_blank" title="Read more about FabLab Network"> FabLab mreža </a>
        </h2>
        <p>
        Fab Lab mreža je otvorena kreativna zajednica proizvođača, umjetnika, naučnika, inženjera, edukatora, studenata, amatera i profesionalaca u više od 100 zemalja i 1800 Fab Lab-ova  <a
                href="https://fablabs.io/labs/map" class="links">širom svijeta</a>. <br>
                Od laboratorija u zajednici do naprednih istraživačkih centara, Fab Lab-ovi dijele cilj demokratizacije pristupa alatima za tehničke izume. Ova zajednica je istovremeno proizvodna mreža, distribuirani kampus tehničkog obrazovanja i distribuirana istraživačka laboratorija koja radi na digitalizaciji proizvodnje, pripremajući izume za narednu generaciju proizvodnje i lične proizvodnje.
        </p><br>

        <span id="foundation"></span>
        <h2>
            <a href="https://fabfoundation.org/" target="_blank" title="Read more about FabLab Foundation"> Fab
            Fondacija
            </a>
        </h2>
        <p>
        Osnovana 2009. godine kako bi se olakšao i podržao rast međunarodne mreže fab lab-ova, kao i razvoj regionalnih organizacija za izgradnju kapaciteta. Fab Fondacija je američka neprofitna organizacija prema članu 501. stav (c) tačka 3., nastala iz Fab Lab Programa MIT-ovog Centra za bitove i atome. Naša misija je da omogućimo pristup alatima, znanju i finansijskim sredstvima za obrazovanje, inovacije i izume koristeći tehnologiju i digitalnu proizvodnju kako bismo omogućili svakome da napravi (gotovo) bilo šta, i na taj način stvara mogućnosti za poboljšanje života i egzistencije širom svijeta. Naši primarni korisnici su organizacije zajednice, obrazovne institucije i neprofitna društva. 
        </p><br>

        <span id="academy"></span>
        <h2>
            <a href="http://fabacademy.org/" target="_blank" title="Read more about Fab Academy"> Fab Akademija </a>
        </h2>
        <p>
        Fab Akademija je intenzivan petomjesečni program koji polaznike uči da osmišljavaju, dizajniraju i prototipiraju projekte koristeći alate i mašine za digitalnu proizvodnju. Riječ je o multidisciplinarnom i praktičnom iskustvu učenja koje osnažuje polaznike da uče kroz rad i inspiriše ih da stvaraju proizvode lokalno, a kako bi postali aktivni učesnici u održivim gradovima i zajednicama.
            <br> 
            Na Fab akademiji ćete naučiti kako zamisliti, prototipirati i dokumentovati svoje ideje kroz mnogo sati praktičnog iskustva s alatima za digitalnu proizvodnju. Uzimamo različite formate koda i pretvaramo ih u fizičke objekte. Fab diploma rezultat je zbira Fab-ovih akademskih certifikata. Napredak u sticanju diplome se ocjenjuje na osnovu stečenih vještina studenta, a ne na osnovu vremena ili kredita.
            <br> 
            Fab Akademija je brzo i praktično iskustvo učenja u kojem studenti planiraju i izvode novi projekat svake sedmice! Svaki pojedinac dokumentuje svoj napredak za svaki projekat, što rezultira ličnim portfoliom tehničkih dostignuća.
            <br> Procjena je da kurs košta 5.000,00 američkih dolara, s tim da cijena varira ovisno o lokaciji i dostupnim mogućnostima stipendiranja. Svi materijali za kurs su javno pohranjeni na adresi  <a href="http://archive.fabacademy.org/"
                class="links">ovdije.</a>
        </p><br>
