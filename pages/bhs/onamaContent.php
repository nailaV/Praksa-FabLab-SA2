        <h2>
            <a href="https://fablabs.io/organizations/association-fablab-bosnia-and-herzegovina-d930f251-a5e3-45d8-959d-a6c3bc0531be"
                target="_blank"> Naše vrijednosti i filozofija </a>
        </h2>

        <p>
        FabLab BiH je organizacija za istraživanje i tehnologiju vođena inovacijama koja razvija inovativne i istraživačke platforme s ciljem poboljšanja istraživačke infrastrukture, razvoja najsavremenijih tehnologija i ubrzanja inovacija u Bosni i Hercegovini (BiH).
            <br>
            <b> FabLab BiH je nezavisna, neprofitna, nevladina i apolitična organizacija. </b>

        <p>

        FabLab BiH istražuje ključne transformativne tehnologije iz Industrije 4.0 i Industrije 5.0, s fokusom na aditivnu proizvodnju (AM), umjetnu inteligenciju (AI), 3D Bioprinting, tehnologije proširene i virtuelne stvarnosti (AR/VR) i osnove iz koncepata cirkularne ekonomije.
            <br>
            Naša glavna katalizatorska partnerstva potiču iz akademske zajednice i industrije, omogućavajući efekte iskorištenja i ubrzanje društvenih ishoda i novih razvoja u polju digitalne proizvodnje kroz veze sa postojećim organizacijama visokog kapaciteta..
            <br> 
            U skladu s našom misijom, FabLab BiH provodi istraživanja koja unapređuju naučna otkrića i tehnološke inovacije stvarajući revolucionarne tehnološke platforme u BiH.
            <br>
            Okruženje za inovacije je okosnica naše organizacije, a zajedno sa ekspertizom u oblasti intelektualnog vlasništva, omogućava nam da katalizujemo našu aktivnost u prosperitet.
        </p>
        <p></p>
        <br>


        <!-- <div id="goto"></div> -->
        <h2>Naše strategije rasta</h2>
        <p align="justify">
        Restrukturirali smo naš model rasta u cilju pomaka, ubrzanja i proširenja prema Industriji 4.0 i 5.0 tehnologijama koristeći našu jedinstvenu poziciju na ukrštanju tehnologije i industrija kao prvi službeno priznati FabLab u zemlji, odnosno  <a href="https://fablabs.io/labs/FabLabSA" target="_blank" class="links">
                "FabLab
                Sarajevo" </a>
      
        <br> <br> 
        Cilj našeg horizontalnog rasta jeste uspostavljanje više FabLab prostora širom BiH kroz saradnju sa lokalnim partnerima. Novi fab-ovi bi omogućili studentima, poduzetnicima, industriji u drugim gradovima da steknu praktične informacije o najnovijoj visokoj tehnologiji iz digitalne proizvodnje, učenju, poboljšanju STEM vještina, izgradnji podsticajnih poduzetničkih ekosistema, razvoju i lansiranju novih proizvoda i usluga kroz saradnju u kreativnom i zabavnom okruženju.
        <br> <br> Udruženje FabLab formirano je u januaru 2020. godine i centralno je tijelo za fab lab-ove koji djeluju u BiH. 
        <a href="https://fablabs.io/organizations/association-fablab-bosnia-and-herzegovina-d930f251-a5e3-45d8-959d-a6c3bc0531be"
            target="_blank" class="links">
            Zvanično je priznat </a> od strane FabLab fondacije i FabLab zajednice. Snažno smo opredijeljeni za principe jednakosti, različitosti i inkluzije. 
            Detaljnije o našim <a href="?page=pages/en/declarations" target="_blank" class="links">vodećim principima</a>.
        <div id="mission">
            </p>
            <br>



            <h2>Naša misija i vizija</h2>
            <p>
            Čvrsto vjerujemo da su ljudi u BiH glavni akteri u vlastitom razvoju, dajući prednost izgradnji kapaciteta i pristupu odozdo prema gore u odnosu na zamjenska rješenja, odnosno doprinos kvalitetu života građana u odnosu na prakse koje nisu dugoročno održive. Cilj nam je osigurati održive, inovativne odgovore na izazove tehnologije i učešća zajednice u nepristupačnom okruženju u BiH.
                <br>
                <br>
                <b>

                    <b class="lightPurple">Misija: </b> Osigurati prostor, opremu i programe koji razvijaju lokalnu industriju, podstiču preduzetništvo, pružaju praktično iskustvo učenja i povećavaju interesovanje za STEM obrazovanje.
                    <br> <br>
                    <b class="lightPurple">Vizija: </b> Djelovati kao nacionalni referentni centar za digitalnu fabrikaciju koji će doprinijeti stvaranju otvorenog i stimulativnog okruženja za razvoj digitalnih vještina, znanja i poduzetništva svih građana u BiH.
                    <br>
                    <br>
                    <b class="lightPurple">Za šta se zalažemo: </b> Učiniti nauku i inovacije otvorenim za sve.
                </b> <br>
            <p>
            Želimo potaknuti razvoj tehnološkog, obrazovnog i kreativnog sektora u BiH pomažući u razvoju esencijalnih vještina za budućnost i podržavajući dobrobit svih.
            </p>
            <p></p><br><br>

