    <div class="DContent">
      <b class="w3-text-ljubicasta">Šta je fab lab?</b>
      <p class="tab">Fab lab-ovi su globalna mreža lokalnih laboratorija koja omogućava izume kroz pružanje pristupa alatima za digitalnu proizvodnju.</p>

      <b class="w3-text-ljubicasta">Šta se nalazi u fab lab-ovima?</b>
      <p class="tab">Fab lab-ovi dijele fluidan inventar osnovnih sposobnosti da se napravi (gotovo) bilo šta, omogućavajući razmjenu ljudi i projekata.</p>

      <b class="w3-text-ljubicasta">Šta pruža mreža fab lab-ova?</b>
      <p class="tab">Operativna, obrazovna, tehnička, finansijska i logistička pomoć izvan onoga što je dostupno unutar jedne laboratorije.</p>

      <b class="w3-text-ljubicasta">Ko može koristiti fab lab?</b>
      <p class="tab">Fab lab-ovi su dostupni kao resurs zajednice, nudeći otvoren pristup za pojedince, kao i planirani pristup programima.</p>

      <b class="w3-text-ljubicasta">Koje su vaše odgovornosti? </b>
      <p class="tab">
        <i> sigurnost</i>: bez oštećenja po ljude ili mašine.<br>
        <i> operacije </i> pomažu u čišćenju, održavanju i poboljšanju laboratorije.<br>
        <i> znanje </i> doprinosi dokumentovanju i podučavanju.
      </p>

      <b class="w3-text-ljubicasta">Ko posjeduje fab lab izume?</b>
      <p class="tab">Dizajni i procesi razvijeni u fab lab-ovima mogu se zaštititi i prodati po izboru pronalazača, s tim da trebaju ostati dostupni pojedincima za korištenje i učenje.</p>

