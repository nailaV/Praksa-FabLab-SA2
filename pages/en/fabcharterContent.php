      <b class="w3-text-ljubicasta">What is fab lab</b>
      <p class="tab">Fab labs are a global network of local labs, enabling invention by providing access to tools for
        digital fabrication.</p>

      <b class="w3-text-ljubicasta">What's in a fab lab?</b>
      <p class="tab">Fab labs share an evolving inventory of core capabilities to make (almost) anything, allowing
        people
        and projects to be shared.</p>

      <b class="w3-text-ljubicasta">What does the fab lab network provide?</b>
      <p class="tab">Operational, educational, technical, financial, and logistical assistance beyond what's available
        within one lab.</p>

      <b class="w3-text-ljubicasta">Who can use a fab lab?</b>
      <p class="tab">Fab labs are available as a community resource, offering open access for individuals as well as
        scheduled access for programs.</p>

      <b class="w3-text-ljubicasta">What are your responsibilities?</b>
      <p class="tab">
        <i> safety</i>: not hurting people or machines. <br>
        <i> operations</i> assisting with cleaning, maintaining, and improving the lab. <br>
        <i> knowledge</i> contributing to documentation and instruction.
      </p>

      <b class="w3-text-ljubicasta">Who owns fab lab inventions?</b>
      <p class="tab">Designs and processes developed in fab labs can be protected and sold however an inventor chooses,
        but should remain available for individuals to use and learn from.</p>

