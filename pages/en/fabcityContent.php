        <h3>LOCALLY PRODUCTIVE AND GLOBALLY CONNECTED SELF-SUFFICIENT CITY</h3>
       
<h2>What is the Fab City Initiative?</h2>
	<tr><td>
<p>Fab City is a global initiative with an aim to develop <b> locally productive </b> and<b> globally connected self-sufficient cities.</b>
Fab City was initially launched in 2014 by Fab Lab Barcelona, allied with the MIT Center for Bits and Atoms, the Fab Foundation and the Institute for Advanced Architecture of Catalonia (IAAC).
						
Fab City is working on the reinvention of the current linear economy paradigm to a Circular Economy.</p>
	
<h3>Smart City with top-down approach / Fab City with bottom-up approach</h3>
 <p>The Fab City Global Initiative started in 2014, powered at the Institute for Advanced Architecture of Catalonia (IAAC), with the mission to establish a 40-year roadmap to develop new, sustainable growth models for cities - cities that produce everything they consume locally, while sharing knowledge globally. In 2054, this process should be so advanced that Fab Cities only import and export data - achieved a self-sustaining circular economy in the urban area. According to the United Nations, by 2050, 75% of the world population is predicted to live in urban settlements where current urbanization processes are being based on consumerism and a linear economy with destructive social, environmental, and economic impacts. Cities are direct and indirect leading energy consumers and polluters of the environment. Through new, more economical solutions, Fab City is establishing new ways of how cities should function. </p>
	                    
<h3>FabLab and the innovation-based economy</h3>
<p> FabLab is primarily a potential for the development of entrepreneurship and job creation, and thus for the economic development of local and regional communities.<br> 
FabLab encourages interdisciplinary collaboration, experimentation, and learning. It creates an environment for innovation and provides a space to bring people together around a common goal. It is a place for the realization of ideas and prototyping with the application of advanced technology (3D printers, 3D scanners, electronics, CNC machines, etc.).<br> 
The Fab City takes the ideals of the Fab Lab such as the connectivity, culture, glocalism and creativity and scales it to the City.</p>
<br>
</td></tr>		 
<h3>Data In Data Out’ (DIDO) principle</h3>
<p>
It is a new urban model of transforming and shaping cities that shifts how they source and use materials from ‘Products In Trash Out’ (PITO) to ‘Data In Data Out’(DIDO). <br></p>
<h3>Industry 4.0</h3>
<p>A shift in the production scenario towards a more ‘mobile’ production model that uses technologies such as IoT, 3D printers, mobile devices and big data.</p> <br><br>
<h3>Which steps Sarajevo needs to take to become a Fab City?</h3>
<p>Produce a signed letter by the mayor and/or deputy mayors expressing intentions to join the Fab City initiative, and manifesting the support to the local community promoting it.<br> 
Create a one-minute video in which the mayor and/or deputy mayors announce publicly the reasons why the city is joining Fab City.
The video will be projected during the Fab City Summit. <br> 
Have a representative of the city present at the Fab City Summit to participate in the public commitment event. This representative could be either a city official, or community members promoting the Fab City initiative.<br>
</p>						
<h3>Fab City Consortium as the local initiative holder</h3>
Fab City pledges are made by local consortiums who represent the ecosystem of the city.	The consortium consists of at least three members: <br>
-Organization as a representative of the local community<br>
-Local FabLab<br>
-City council <br><br>
	   
<h3>After the Fab City Summit</h3>
<p>No more than one week after the Summit, the city must send out a press release about the public commitment made at the Fab Conference and the future plans for Fab City. - Work to connect existing strategies in Sarajevo (circular economy, open data, open innovation, smart city, etc) with the Fab City initiative locally and globally. - Develop a strong policy on open data about city metrics, which will help to build the common infrastructure among cities: the Fab City Dashboard. - Start a process to identify areas of the city that could be experimentation areas for different strategies connected with the Fab City vision, these are often called Fab City prototype. -Logistically or financially support the local community to develop programs, activities, events or projects connected with the Fab City vision. -Participate in the global meetings such as the Fab City Summit.
</p>
