            <p align="justify">
                <strong class="uvodFontSizeFontSize providna"> <i> “FabLab Sarajevo – is a technical prototyping
                        platform for invention,
                        collaboration and learning, and is created to inspire people and entrepreneurs to turn their
                        ideas into new products and prototypes.”</i> </strong><br>
                <br><br>
                Our center of excellence (CoE) in Sarajevo – FabLab Sarajevo – is membership-based, demo center and
                powerhouse established in October 2020 packed with the technology, knowledge, and expertise required to
                bridge the gap between academia and industry. It is the first officially recognized FabLab in the
                country.
