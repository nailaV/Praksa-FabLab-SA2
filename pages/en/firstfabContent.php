            <br><br>
            FabLab Sarajevo seeks to integrate relevant capabilities and encourage Responsible Research and
            Innovation (RRI) cooperation with the wider research community and industry, as well as other public
            sector agencies, towards meaningful and impactful outcomes.
            <br><br>
            Engineering, research and development (R&D) is at the heart of FabLab Sarajevo capability development
            and, therefore, one of the top priorities. FabLab Sarajevo’s R&D activities span from biomedical
            sciences to physical sciences and engineering, at the same time extending capabilities into new domains
            (robots, bio, textile, circular economy, green) which create the opportunity for sustained growth.
            <br><br>
            As an innovation facilitator, FabLab Sarajevo encourages experimentation through new technologies such
            as AR/VR and AI.
            <br><br>
            FabLab Sarajevo unleashes the full potential of R&D and innovation to help our wider community and
            clients create prototypes and smart products, optimize manufacturing operations, and deliver new sources
            of value, allowing the rise of a sustainable Digital Fabrication Industry in the country.
            <br><br>
            We value our people most of all and consider having the right team around us as one of our key ‘tools’.
            The team consists of permanent staff and external experts, but we also have interns from different
            institutes and volunteers with various backgrounds and capabilities.
