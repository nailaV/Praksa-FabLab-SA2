        <span id="history"></span>
        <h2>History of the FabLab </h2>
        <p>
            <strong>“Give ordinary people the right tools, and they will
                design and build the most extraordinary things.”</strong><br>
            <br> That’s the idea behind Fab Labs, which originated in 2001 at <a href="https://www.mit.edu/"
                target="_blank" class="links"> Massachusetts Institute of Technology</a>
            <a href="https://cba.mit.edu/" target="_blank" class="linksDark"> Center for Bits
                and Atoms </a> by Professor<a href="https://en.wikipedia.org/wiki/Neil_Gershenfeld" class="links"
                target="_blank"> Neil Gershenfeld </a>.<br><br> A FabLab, or digital fabrication laboratory, is a place
            to play, to create, to mentor
            and to invent - a place for learning and innovation. FabLabs are also known as Urban or Industry Innovation
            labs.
        </p><br>



        <span id="network"></span>
        <h2>
            <a href="https://fablabs.io/" target="_blank" title="Read more about FabLab Network"> FabLab Network </a>
        </h2>
        <p>
            The Fab Lab Network is an open creative community of fabricators, artists, scientists, engineers, educators,
            students, amateurs and professionals located in more than 100 countries and 1,800 Fab Labs <a
                href="https://fablabs.io/labs/map" class="links">across the globe</a>.
            From community based labs to advanced research centers, Fab Labs share the goal of democratizing access to
            the tools for technical invention. This community is simultaneously a manufacturing network, a distributed
            technical education campus,
            and a distributed research laboratory working to digitize fabrication, inventing the next generation of
            manufacturing and personal fabrication. The Fab Lab Network is an open creative community of fabricators,
            artists, scientists, engineers,
            educators, students, amateurs and professionals located in more than 100 countries and 1,800 Fab Labs across
            the globe.
            <br> From community based labs to advanced research centers, Fab Labs share the goal of democratizing access
            to the tools for technical invention. This community is simultaneously a manufacturing network, a
            distributed technical education
            campus, and a distributed research laboratory working to digitize fabrication, inventing the next generation
            of manufacturing and personal fabrication.
        </p><br>

        <span id="foundation"></span>
        <h2>
            <a href="https://fabfoundation.org/" target="_blank" title="Read more about FabLab Foundation"> Fab
                Foundation
            </a>
        </h2>
        <p>
            Formed in 2009 to facilitate and support the growth of the international fab lab network as well as the
            development of regional capacity-building organizations. The Fab Foundation is a US non-profit 501(c) 3
            organization that emerged from MIT’s Center
            for Bits & Atoms Fab Lab Program. Our mission is to provide access to the tools, the knowledge and the
            financial means to educate, innovate and invent using technology and digital fabrication to allow anyone to
            make (almost) anything, and
            thereby creating opportunities to improve lives and livelihoods around the world. Community organizations,
            educational institutions and non-profit concerns are our primary beneficiaries.
        </p><br>

        <span id="academy"></span>
        <h2>
            <a href="http://fabacademy.org/" target="_blank" title="Read more about Fab Academy"> Fab Academy </a>
        </h2>
        <p>
            Fab Academy is an intensive ﬁve-month program that teaches students to envision, design and prototype
            projects using digital fabrication tools and machines. It is a multi-disciplinary and hands-on learning
            experience that empowers students to learn-by-doing
            and inspires them to make stuff locally to become active participants in sustainable cities and communities.
            <br> At the Fab Academy, you will learn how to envision, prototype and document your ideas through many
            hours of hands-on experience with digital fabrication tools. We take a variety of code formats and turn them
            into physical objects. The
            Fab Diploma is the result of the sum of Fab Academy Certificates. Progress towards the diploma is evaluated
            by a student’s acquired skills rather than time or credits.
            <br> The Fab Academy is a fast paced, hands-on learning experience where students plan and execute a new
            project each week! Each individual documents their progress for each project, resulting in a personal
            portfolio of technical accomplishments
            <br> The course is estimated to cost US$5000, but varies with location and available scholarship
            opportunities. All course materials are publicly archived online <a href="http://archive.fabacademy.org/"
                class="links">here.</a>
        </p><br>
